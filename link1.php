      <p><span style="font-weight: bold">1.</span> <strong>Duerme y descansa lo necesario:</strong> una de las   principales causas de las bolsas en los ojos y ojeras, es la falta de   descanso. Es muy importante dormir al menos 8 horas diarias sin   interrupciones y realizar regularmente actividades que te relajen.</p>
      <p><span style="font-weight: bold">2. </span><strong>Utiliza una o dos almohadas: </strong>dormir con la cabeza   más alta que el cuerpo es ideal para descansar mejor y evitar que el   líquido se acumule con más facilidad en la zona facial. Es recomendado   utilizar una o dos almohadas para evitar los ojos hinchados.</p>
      <p><span style="font-weight: bold">3. </span><strong>Consume alimentos diuréticos y baja el consumo de sal: </strong>una   cena a base de frutas y verduras puede ser una gran forma para eliminar   los líquidos que hay retenidos en el cuerpo. Incluir a diario más   alimentos diuréticos y reducir el consumo de sal, es ideal para combatir   la retención de líquidos y tener un mejor aspecto en los ojos.</p>
      <p><span style="font-weight: bold">4.</span> <strong>Aplicar agua fría en las mañanas:</strong> casi siempre   vemos que los ojos están hinchados en las horas de la mañana. Para esto   es recomendado lavar la cara con agua bien fría o en lo posible aplicar   hielo en la zona afectada.</p>
      <p><span style="font-weight: bold">5.</span> <strong>Utilizar pepino frío: </strong>el pepino es un hidratante   natural de la piel y nos ayuda a lucir un mejor aspecto. Poner algunas   rodajas de pepino en el congelador y después aplicarlas en la zona de   las bolsas, ayuda a reducir la inflamación y a lucir más frescas.</p>
