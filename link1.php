    <div align="justify" class="texto1 Estilo2">
      <p><span style="font-weight: bold">1. </span><strong>Estimula la circulación de tu cuero cabelludo: </strong>para esto sólo basta con cepillar unas 50 veces con movimientos suaves y uniformes, desde la raíz hasta las puntas.</p>
      <p><span style="font-weight: bold">2.</span> <strong>Realiza masajes capilares:</strong> todas las noches antes   de ir a dormir, es recomendado hacerse un masaje capilar de 10 minutos.   Para esto debes usar las palmas y dedos de tus manos con suaves masajes   circulares.</p>
      <p><span style="font-weight: bold">3. </span><strong>Corta las puntas una vez al mes: </strong>las puntas del   cabello suelen sufrir más daños debido a los diferentes factores y es   por eso que es conveniente cortarlas regularmente.</p>
      <p><span style="font-weight: bold">4.</span> <strong>Evita cepillar y/o peinar el cabello húmedo: </strong>cuando   el cabello está húmedo, es más débil y está más propenso a    quebrajarse. Lo recomendado es esperar a que se seque para peinarlo   cuidadosamente.</p>
      <p><span style="font-weight: bold">5. </span><strong>No laves tu pelo todos los días:</strong> los expertos   recomiendan lavar el cabello de dos a tres veces por semana, ya que éste   tarda en recuperarse de los químicos del lavado. Descansar de los   diferentes productos, le permitirá al cabello producir aceites naturales   que ayudarán a su crecimiento.</p>
      <p><span style="font-weight: bold">6. </span><strong>Evita el uso de productos con silicona:</strong> los   productos para el cabello basados en silicona, prometen un cabello más   saludable y sin daño. Sin embargo, se ha comprobado que este tipo de   productos pueden tapar los folículos impidiendo el crecimiento.</p>
